@app.route('/register', methods=['GET', 'POST'])
def register():
    if current_user.is_authenticated:
        return redirect(url_for('index'))
    form = RegistrationForm()
    if form.validate_on_submit():
        username,email,password = form.username.data, form.email.data, form.password.data
        send_registration_check_email(username,email,password)
        flash('Check your email to confirm your account')
        return redirect(url_for('login'))
    return render_template('register.html', title='Register', form=form)

@app.route('/create_user/<token>', methods=['GET', 'POST'])
def create_user(token):
    if current_user.is_authenticated:
        return redirect(url_for('index'))
    payload = jwt.decode(token,app.config['SECRET_KEY'],algorithms=['HS256'])
    user = User(username=payload['username'], email=payload['email'])
    user.set_password(payload['password'])
    db.session.add(user)
    db.session.commit()
    flash('Congratulations, you are now a registered user!')
    return redirect(url_for('login'))

